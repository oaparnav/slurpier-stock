package myproblems;

public class Money {
	
	int rupees;
	int count;
	
	public Money(int rupees, int count) {
		this.rupees = rupees;
		this.count = count;
	}

	public int getRupees() {
		return rupees;
	}

	public void setRupees(int rupees) {
		this.rupees = rupees;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	
	
}
